/** Returns countries with pinned common ones at the top, then alphabetical (no dupes). */
export function getOrderedCountries(): { pinned: Country[]; rest: Country[] } {
  const pinned = PINNED_COUNTRIES.map(
    code => ALL_COUNTRIES.find(c => c.code === code)!
  ).filter(Boolean);
  const rest = ALL_COUNTRIES.filter(c => !PINNED_COUNTRIES.includes(c.code))
    .sort((a, b) => a.name.localeCompare(b.name));
  return { pinned, rest };
}

// ============= Submit =============

/**
 * Persist a charter enquiry to the internal Lovable Cloud DB, then trigger
 * the `send-charter-enquiry` edge function which emails charter@eclipseaircharter.com.
 * Email failures are non-fatal — the enquiry is already saved.
 */
export async function submitCharterEnquiry(input: CharterEnquiryInput): Promise<{ id: string }> {
  const { data: { user } } = await supabase.auth.getUser();

  // Generate the id client-side so we don't need a SELECT-after-insert
  // (anon role has no SELECT policy on charter_enquiries).
  const id =
    typeof crypto !== "undefined" && "randomUUID" in crypto
      ? crypto.randomUUID()
      : `${Date.now()}-${Math.random().toString(36).slice(2)}`;

  const payload = {
    id,
    trip_type: input.trip_type,
    legs: input.legs as unknown as never, // jsonb
    return_date: input.return_date,
    return_hour: input.return_hour,
    passengers: input.passengers,
    contact_name: input.contact.name,
    contact_email: input.contact.email,
    contact_phone: input.contact.phone || null,
    contact_country: input.contact.country,
    preferred_contact: input.contact.preferred_contact,
    submitted_by_user_id: user?.id ?? null,
    user_agent: typeof navigator !== "undefined" ? navigator.userAgent : null,
    referrer: typeof document !== "undefined" ? document.referrer || null : null,
  };

  const { error } = await supabase.from("charter_enquiries").insert(payload);

  if (error) throw error;

  // Fire-and-forget email send. Don't block UI on email delivery.
  supabase.functions
    .invoke("send-charter-enquiry", { body: { enquiry_id: id } })
    .then(({ error: fnErr }) => {
      if (fnErr) console.warn("[charter-enquiry] email send failed:", fnErr);
    })
    .catch((err) => console.warn("[charter-enquiry] email invoke threw:", err));

  return { id };
}
