import { z } from "zod";
import { supabase } from "@/integrations/supabase/client";

// ============= Types =============

export type TripType = "one_way" | "multi_city";
export type ContactMethod = "call" | "email" | "whatsapp";

export interface CharterLeg {
  from_icao: string;
  from_label: string;
  to_icao: string;
  to_label: string;
  depart_date: string; // YYYY-MM-DD
  depart_hour: number | null; // 0-23 or null
}

export interface CharterContact {
  name: string;
  email: string;
  phone: string;
  country: string;
  preferred_contact: ContactMethod;
}

export interface CharterEnquiryInput {
  trip_type: TripType;
  legs: CharterLeg[];
  return_date: string | null; // YYYY-MM-DD
  return_hour: number | null; // 0-23
  passengers: number;
  contact: CharterContact;
}

// ============= Validation =============

export const contactSchema = z.object({
  name: z.string().trim().min(1, "Name is required").max(80, "Name too long"),
  email: z.string().trim().email("Invalid email address").max(255),
  phone: z.string().trim().max(40).optional().or(z.literal("")),
  country: z.string().trim().min(1, "Please select a country").max(80),
  preferred_contact: z.enum(["call", "email", "whatsapp"], {
    errorMap: () => ({ message: "Select a contact method" }),
  }),
});

// ============= Hour formatting =============

/** Format hour 0-23 as "12:00 AM" / "1:00 AM" / ... / "11:00 PM" */
export function formatHour(hour: number): string {
  if (hour === 0) return "12:00 AM";
  if (hour === 12) return "12:00 PM";
  if (hour < 12) return `${hour}:00 AM`;
  return `${hour - 12}:00 PM`;
}

export const HOURS_0_TO_23 = Array.from({ length: 24 }, (_, i) => i);

